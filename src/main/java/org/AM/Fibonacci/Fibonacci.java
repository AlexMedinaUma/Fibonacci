package org.AM.Fibonacci;

/**
 * Class providing a method that computes Fibonacci on a natural number
 *
 * @author Alejandro Medina Astorga
 */

public class Fibonacci {
    public int compute(int value) {
        int result = 0;
        if(value == 0){
            result = 0;
        }
        if(value == 1){
            result = 1;
        }



        return result;
    }
}
